package Ch02_Operator;

public class Ch02_Operator04 {
	public static void main(String[] args) {
		/*
		 * 비교 연산자
		 * 비교를 해서 참인지 거짓인지 확인하는게 목적.
		 * 결과 값은 true or false.
		 */
		
		int a = 10;
		int b = 20;
		int c = 30;
		
		System.out.println(a < b); // true
		System.out.println(a > b); // false
		System.out.println(a + b <= c); // true
		System.out.println(a + b >= c); // true
	}
}
